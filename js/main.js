function mergeLanguagesFromFile(data) {
  const langs = data && data.languages;
  if (!langs) return;
  for (const key of ["fi", "sv", "en"]) {
    if (!langs[key]) continue;
    STRINGS[key] = { ...STRINGS[key], ...langs[key] };
  }
}

function imgPath(filename) {
  if (!filename) return "";
  const s = String(filename).trim();
  if (s.startsWith("http://") || s.startsWith("https://")) return s;
  const clean = s.replace(/^\/+/, "").replace(/^images\//, "");
  return `images/${clean}`;
}

